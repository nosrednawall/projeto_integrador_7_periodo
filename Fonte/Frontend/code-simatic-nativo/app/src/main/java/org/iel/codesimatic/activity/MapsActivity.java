import org.iel.codesimatic.R;

public class MapsActivity extends FragmentActivity implements OnMapReadyCallback {

    private GoogleMap mMap;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_maps);

        // Obtain the SupportMapFragment and get notified when the map is ready to be used
        SupportMapFragment mapFragment = (SupportMapFragment) getSupportFragmentManager().findFragmentById(R.id.map);
        mapFragment.getMapAsync(this);

    }

    @Override
    public void onMapReady(GoogleMap googleMap) {
        mMap = googleMap;

        LatLng iel = new LatLng(-25.514541, -49.179868);
        mMap.addMarker(new MarkerOptions().position(iel).title("Marcado em Faculdades da ind√∫stria"));
        mMap.moveCamera(CameraUpdateFactory.newLatLng(iel));
    }
}
